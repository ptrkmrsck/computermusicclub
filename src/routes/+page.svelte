	<h1>computermusic.club</h1>
	<p>
		The times that I am most motivated to make things is after talking to people that are excited
		and curious about making things. It feels like connecting with people really seamlessly flows
		into connecting ideas, and what is making a thing if not the simple act of connecting ideas?
	</p>

	<p>
		Selfishly, I want more of this for myself, and to have some regularity around it, along with a
		little splash of accountability. Non-selfishly, I have heard from a few folks in the Twin Cities
		that they feel isolated in their computer-music-related creative pursuits. There is so much
		knowledge and experience here, but there isn’t necessarily an easy way to exchange this
		knowledge.
	</p>

	<p>
		These are <em>some</em> of the reasons why I want computermusic.club to exist (there are MANY more!),
		but I also think it is important to get as many perspectives on what would most benefit this “community”
		as possible. For that reason, I imagine this first meeting being more of an open forum. Anyone that
		has ideas or questions about the concept will have a chance to be heard. From there we will decide
		which things to try to make happen first, and then figure out how to make those things happen. Very
		exciting!
	</p>

	<p>
		I am planning on recording and/or taking notes on the discussion, and generally being really
		“open source” about the whole thing. Folks can contribute in whatever way they have the capacity
		to.
	</p>

	<p>Let’s learn and make stuff with computers!</p>
